// Sum of numbers in a given range (10 to 50) using a do-while loop

let num = 10; // Starting number
let sum = 0; // Initialize sum to 0

// Using do-while loop to sum numbers from 10 to 50
do {
  sum += num; // Add current number to sum
  num++; // Increment num by 1
} while (num <= 50);

console.log(sum); // Output the sum
